import turtle

def draw_maze():
    # 设置画布
    screen = turtle.Screen()
    screen.setup(width=600, height=600)
    screen.title("60-unit Grid Maze")
    
    # 创建海龟
    t = turtle.Turtle()
    t.speed(0)  # 最快速度
    t.pensize(5)
    
    # 调整坐标系
    t.penup()
    t.goto(-300, 300)  # 起始点
    t.pendown()
    
    # 所有线段数据 (x1, y1, x2, y2)，以60为单位
    lines = [
        # 第一行 (y=0)
        (0, 0, 60, 0), (600, 0, 600, 60),
        (60, 0, 120, 0), (60, 0, 60, 60),
        (120, 0, 180, 0), (120, 0, 120, 60),
        (180, 0, 240, 0), (240, 0, 300, 0), (240, 0, 240, 60),
        (300, 0, 360, 0), (360, 0, 420, 0), (360, 0, 360, 60),
        (420, 0, 480, 0), (420, 0, 420, 60), (480, 0, 540, 0),
        (540, 0, 600, 0),
        
        # 第二行 (y=60)
        (0, 60, 0, 120), (600, 60, 600, 120),
        (120, 60, 120, 120), (180, 60, 180, 120),
        (240, 60, 300, 60), (300, 60, 300, 120),
        (360, 60, 360, 120), (480, 60, 540, 60),
        (540, 60, 600, 60),
        
        # 第三行 (y=120)
        (0, 120, 0, 180), (600, 120, 600, 180),
        (60, 120, 120, 120), (180, 120, 240, 120),
        (240, 120, 300, 120), (360, 120, 420, 120),
        (420, 120, 420, 180), (480, 120, 540, 120),
        (540, 120, 600, 120),
        
        # 第四行 (y=180)
        (0, 180, 0, 240), (600, 180, 600, 240),
        (60, 180, 120, 180), (120, 180, 180, 180),
        (180, 180, 180, 240), (180, 180, 240, 180),
        (240, 180, 240, 240), (240, 180, 300, 180),
        (300, 180, 300, 240), (300, 180, 360, 180),
        (420, 180, 420, 240), (420, 180, 480, 180),
        (540, 180, 600, 180),
        
        # 第五行 (y=240)
        (0, 240, 0, 300), (600, 240, 600, 300),
        (60, 240, 120, 240), (120, 240, 120, 300),
        (240, 240, 240, 300), (360, 240, 360, 300),
        (420, 240, 420, 300), (420, 240, 480, 240),
        (480, 240, 540, 240),
        
        # 第六行 (y=300)
        (0, 300, 0, 360), (600, 300, 600, 360),
        (60, 300, 60, 360), (60, 300, 120, 300),
        (120, 300, 180, 300), (180, 300, 180, 360),
        (240, 300, 300, 300), (300, 300, 360, 300),
        (360, 300, 420, 300), (420, 300, 420, 360),
        (480, 300, 540, 300), (540, 300, 600, 300),
        
        # 第七行 (y=360)
        (0, 360, 0, 420), (600, 360, 600, 420),
        (120, 360, 120, 420), (120, 360, 180, 360),
        (180, 360, 180, 420), (240, 360, 240, 420),
        (300, 360, 360, 360), (360, 360, 420, 360),
        (420, 360, 480, 360), (540, 360, 540, 420),
        
        # 第八行 (y=420)
        (0, 420, 0, 480), (600, 420, 600, 480),
        (0, 420, 60, 420), (120, 420, 120, 480),
        (240, 420, 240, 480), (240, 420, 300, 420),
        (300, 420, 300, 480), (360, 420, 420, 420),
        (420, 420, 420, 480), (480, 420, 480, 480),
        (480, 420, 540, 420), (540, 420, 540, 480),
        (540, 420, 600, 420),
        
        # 第九行 (y=480)
        (0, 480, 0, 540), (600, 480, 600, 540),
        (0, 480, 60, 480), (60, 480, 60, 540),
        (60, 480, 120, 480), (120, 480, 120, 540),
        (120, 480, 180, 480), (180, 480, 180, 540),
        (240, 480, 240, 540), (360, 480, 360, 540),
        (360, 480, 420, 480), (480, 480, 480, 540),
        (540, 480, 540, 540),
        
        # 第十行 (y=540) 和底部边界 (y=600)
        (0, 600, 60, 600), (0, 540, 0, 600),
        (60, 600, 120, 600), (120, 600, 180, 600),
        (180, 600, 240, 600), (180, 540, 240, 540),
        (240, 600, 300, 600), (300, 600, 360, 600),
        (300, 540, 300, 600), (300, 540, 360, 540),
        (360, 600, 420, 600), (420, 600, 480, 600),
        (420, 540, 480, 540), (480, 600, 540, 600),
        (540, 600, 600, 600)
    ]
    
    # 绘制所有线段
    for line in lines:
        x1, y1, x2, y2 = line
        t.penup()
        t.goto(x1 - 300, 300 - y1)  # 调整坐标
        t.pendown()
        t.goto(x2 - 300, 300 - y2)
    
    # 添加起点和终点标记
    t.penup()
    t.goto(-270, 270)  # 起点 (左上角附近)
    t.dot(20, "green")
    t.goto(270, -270)  # 终点 (右下角附近)
    t.dot(20, "red")

    t.hideturtle()  # 隐藏海龟

# 运行绘制函数
draw_maze()